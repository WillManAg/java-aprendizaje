package semanados;

public class ejerciciowhile {

	public static void main(String[] args) {
		
		// Hacer que cuente los numeros del 1 al 10
		
		int numero = 1;
		
		while(numero <= 10) {
			
			System.out.println(numero);
			
			numero++;
			
		}
		
		// Hacer que cuente los pasos del 10 al 1
		
		int otronum = 10;
		
		while(otronum >= 1) {
			
			System.out.println(otronum);
			
			otronum--;
			
		}

	}

}