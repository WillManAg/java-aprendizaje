package semanatres;

public class empezandofor {

	public static void main(String[] args) {
		
		// creciente

		for (int i=1; i<11; i++) {
			
			System.out.print(i + " ");
		}

		System.out.println();
		// decreciente
		
		for (int i=10; i>0; i--) {
			
			System.out.print(i + " ");
		}
		
		System.out.println();
		// pares
		
		for (int i=2; i<11; i+=2) {
			
			System.out.print(i + " ");
		}
		
		
	}

}
